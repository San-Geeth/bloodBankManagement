package Models;

/**
 *
 * @author erand
 */
public class NurseVer {
    
    private String NurName;
    private int NurPhNumber;
    private String NurEmail;
    private int NId;
    private String NIC;

    public String getNurName() {
        return NurName;
    }

    public void setNurName(String NurName) {
        this.NurName = NurName;
    }

    public int getNurPhNumber() {
        return NurPhNumber;
    }

    public void setNurPhNumber(int NurPhNumber) {
        this.NurPhNumber = NurPhNumber;
    }

    public String getNurEmail() {
        return NurEmail;
    }

    public void setNurEmail(String NurEmail) {
        this.NurEmail = NurEmail;
    }

    public int getNId() {
        return NId;
    }

    public void setNId(int NId) {
        this.NId = NId;
    }

    public String getNIC() {
        return NIC;
    }

    public void setNIC(String NIC) {
        this.NIC = NIC;
    }
       
}
